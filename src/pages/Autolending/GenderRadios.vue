<template>
	<div class="gender-radios">
		<h3 class="filter-title">
			Gender
		</h3>
		<kv-radio
			id="gender-radio-both"
			name="gender-radio"
			radio-value="both"
			v-model="gender"
			class="filter-radio"
		>
			Everyone
		</kv-radio>
		<kv-radio
			id="gender-radio-female"
			name="gender-radio"
			radio-value="female"
			v-model="gender"
			class="filter-radio"
		>
			Women only
		</kv-radio>
		<kv-radio
			id="gender-radio-male"
			name="gender-radio"
			radio-value="male"
			v-model="gender"
			class="filter-radio"
		>
			Men only
		</kv-radio>
	</div>
</template>

<script>
import _get from 'lodash/get';
import gql from 'graphql-tag';
import KvRadio from '@/components/Kv/KvRadio';

export default {
	inject: ['apollo'],
	components: {
		KvRadio,
	},
	data() {
		return {
			gender: 'both',
		};
	},
	apollo: {
		query: gql`{
			autolending @client {
				currentProfile {
					loanSearchCriteria {
						filters {
							gender
						}
					}
				}
			}
		}`,
		preFetch: true,
		result({ data }) {
			this.gender = _get(data, 'autolending.currentProfile.loanSearchCriteria.filters.gender') || 'both';
		},
	},
	watch: {
		gender(gender, previousGender) {
			if (gender !== previousGender) {
				this.apollo.mutate({
					mutation: gql`mutation {
						autolending @client {
							editProfile(profile: {
								loanSearchCriteria: {
									filters: {
										gender: ${gender === 'both' ? null : gender}
									}
								}
							})
						}
					}`,
				});
			}
		}
	},
};
</script>

